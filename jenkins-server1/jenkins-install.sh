#!/bin/bash

set -e

# Update and install Java
echo "Updating package lists and installing Java..."
sudo apt update
sudo apt install openjdk-11-jre -y

# Verify Java installation
echo "Verifying Java installation..."
java -version

# Jenkins installation
echo "Adding Jenkins key and repository..."
curl -fsSL https://pkg.jenkins.io/debian/jenkins.io-2023.key | sudo tee /usr/share/keyrings/jenkins-keyring.asc > /dev/null
echo deb [signed-by=/usr/share/keyrings/jenkins-keyring.asc] https://pkg.jenkins.io/debian binary/ | sudo tee /etc/apt/sources.list.d/jenkins.list > /dev/null

echo "Updating package lists and installing Jenkins..."
sudo apt-get update
sudo apt-get install jenkins -y

# Verify Jenkins installation
echo "Starting Jenkins service..."
sudo systemctl start jenkins

echo "Checking Jenkins service status..."
if sudo systemctl status jenkins | grep -q "active (running)"; then
    echo "Jenkins is running successfully."
else
    echo "Jenkins failed to start. Checking logs..."
    sudo journalctl -xeu jenkins.service
    exit 1
fi

# Terraform Installation
echo "Installing Terraform..."
sudo apt-get update && sudo apt-get install -y gnupg software-properties-common
wget -O- https://apt.releases.hashicorp.com/gpg | gpg --dearmor | sudo tee /usr/share/keyrings/hashicorp-archive-keyring.gpg
gpg --no-default-keyring --keyring /usr/share/keyrings/hashicorp-archive-keyring.gpg --fingerprint
echo "deb [signed-by=/usr/share/keyrings/hashicorp-archive-keyring.gpg] https://apt.releases.hashicorp.com $(lsb_release -cs) main" | sudo tee /etc/apt/sources.list.d/hashicorp.list
sudo apt update
sudo apt-get install terraform -y

# Installing kubectl
echo "Installing kubectl..."
curl -LO "https://dl.k8s.io/release/$(curl -L -s https://dl.k8s.io/release/stable.txt)/bin/linux/amd64/kubectl"
curl -LO "https://dl.k8s.io/release/$(curl -L -s https://dl.k8s.io/release/stable.txt)/bin/linux/amd64/kubectl.sha256"
echo "$(cat kubectl.sha256)  kubectl" | sha256sum --check
sudo install -o root -g root -m 0755 kubectl /usr/local/bin/kubectl

# Install AWS CLI
echo "Installing AWS CLI..."
sudo apt install unzip -y
curl "https://awscli.amazonaws.com/awscli-exe-linux-x86_64.zip" -o "awscliv2.zip"
unzip awscliv2.zip
sudo ./aws/install

echo "Installation script completed."
